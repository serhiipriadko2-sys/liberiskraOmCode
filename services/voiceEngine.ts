/**
 * Determines the active voice based on a priority cascade.
 * @param metrics - The current IskraMetrics.
 * @returns The dominant Voice object.
 */
export function getActiveVoice(metrics: IskraMetrics): Voice {
  for (const voice of VOICES) {
    if (voice.activation(metrics)) {
      return voice;
    }
  }
  return VOICES.find(v => v.name === 'ISKRA')!; // Fallback to ISKRA
}

/**
 * Gets the system instruction prompt for a given voice.
 * @param voice - The active Voice object.
 * @returns The system instruction string.
 */
export function getSystemInstructionForVoice(voice: Voice): string {
  return VOICE_PROMPTS[voice.name] || VOICE_PROMPTS['ISKRA'];
}
